def build_squad_relations(squad, starting_squad):
  built = []
  for i in range(len(squad["relations"])):
    built.append({ "position": squad["positions"][i], "player": starting_squad[i], "relations": [] })

    for j in range(len(squad["relations"][i])):
      if squad["relations"][i][j] == 1:
        built[i]["relations"].append({"player": starting_squad[j], "position": squad["positions"][j]})

  return built

